    private void srediTabelu() {
        
        ModelTblStavkeNarudzbine mtsn = new ModelTblStavkeNarudzbine(narudzbina);
        tblStavkeNarudzbine.setModel(mtsn);
        
        TableColumn tableColumnArtikli = tblStavkeNarudzbine.getColumnModel().getColumn(1);    
        try {
            JComboBox comboBoxArtikli = new JComboBox(new DefaultComboBoxModel(Kontroler.getInstance().vratiArtikle().toArray()));
            tableColumnArtikli.setCellEditor(new DefaultCellEditor(comboBoxArtikli));
            tblStavkeNarudzbine.getModel().addTableModelListener(new TableModelListener() {
                @Override
                public void tableChanged(TableModelEvent e) {
                    txtUkupnaVrednost.setText(narudzbina.getUkupnaVrednost()+ "");
                }
            });
            
        } catch (Exception ex) {
            Logger.getLogger(FrmNarudzbinePojedinacno.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private boolean validacija() {
        boolean validator = true;
        String brojNarudzbine = txtBrojNarudzbine.getText();
        try {
            int brojNarudzbineIntTip = Integer.parseInt(brojNarudzbine);
            if (brojNarudzbineIntTip <= 0) {
                throw new NumberFormatException();
            }
            lblBrojNarudzbineVal.setVisible(false);
        } catch (NumberFormatException ex) {
            validator = false;
            lblBrojNarudzbineVal.setVisible(true);
        }
        String datumString = txtDatum.getText();
        try {
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd.MM.yyyy.");
            formatter.withLocale(Locale.ITALY);
            LocalDate datum = LocalDate.parse(datumString, formatter);
            lblDatumVal.setVisible(false);
        } catch (Exception ex) {
            validator = false;
            lblDatumVal.setVisible(true);
        }
        if (narudzbina.getStavkeNarudzbine().size() < 1) {
            validator = false;
        }
        lblStavkeNarudzbineVal.setVisible(narudzbina.getStavkeNarudzbine().size() < 1);
        
        if (!narudzbina.getStavkeNarudzbine().isEmpty() && 
                (narudzbina.getStavkeNarudzbine().get(narudzbina.getStavkeNarudzbine().size() - 1).getArtikal() == null ||
                narudzbina.getStavkeNarudzbine().get(narudzbina.getStavkeNarudzbine().size() - 1).getKolicina() <= 0)) {
            validator = false;
        }
        
        lblStavkeNarudzbineVal.setVisible(!narudzbina.getStavkeNarudzbine().isEmpty() && (narudzbina.getStavkeNarudzbine().get(narudzbina.getStavkeNarudzbine().size() - 1).getArtikal()== null || narudzbina.getStavkeNarudzbine().get(0).getKolicina() <= 0));
        return validator;
    }

