    private void btnSacuvajNarudzbinuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajNarudzbinuActionPerformed
        if (!validacija()) {
            return;
        }
        narudzbina.setNarudzbinaID(Integer.parseInt(txtBrojNarudzbine.getText()));        
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd.MM.yyyy.");
        formatter.withLocale(Locale.ITALY);
        LocalDate datum = LocalDate.parse(txtDatum.getText(), formatter);
        narudzbina.setDatum(datum);  
        narudzbina.setUkupnaVrednost(Double.parseDouble(txtUkupnaVrednost.getText()));
        narudzbina.setKonobar((Konobar) cbKonobari.getSelectedItem());
        narudzbina.setStornirana((boolean) cbStornirana.getSelectedItem());
        narudzbina.setSto((Sto) cbStolovi.getSelectedItem());
        for (StavkaNarudzbine stavka : narudzbina.getStavkeNarudzbine()) {
            stavka.setNarudzbina(narudzbina);
        }
        if (mod == Mod.NEW) {
            try {
                Kontroler.getInstance().kreirajNarudzbinu(narudzbina);
                JOptionPane.showMessageDialog(rootPane, "Narudzbina uspesno kreirana.");
                mtn.dodajNarudzbinu(narudzbina);
            } catch (Exception ex) {
                Logger.getLogger(FrmNarudzbinePojedinacno.class.getName()).log(Level.SEVERE, null, ex);
            }            
        }
        
        if (mod == Mod.EDIT) {
            ModelTblStavkeNarudzbine mtsn = (ModelTblStavkeNarudzbine) tblStavkeNarudzbine.getModel();
            mtsn.izmeniNarudzbinu();
            try {
                Narudzbina nar = Kontroler.getInstance().izmeniNarudzbinu(narudzbina);
                JOptionPane.showMessageDialog(rootPane, "Narudzbina uspesno izmenjena.");
            } catch (Exception ex) {
                Logger.getLogger(FrmNarudzbinePojedinacno.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        dispose();        
        
    }//GEN-LAST:event_btnSacuvajNarudzbinuActionPerformed


