    private void srediFormu() {
        srediTabeluKlijenti();
        srediParametre();
    }

    private void srediTabeluKlijenti() {
        ModelTblKlijenti mtk = new ModelTblKlijenti(Session.getInstance().getKlijenti());
        tblKlijenti.setModel(mtk);
        Session.getInstance().setMtk(mtk);
    }

    private void srediParametre() {
        try {
            txtPassword.setText(Util.getInstance().getPassword());
            txtUsername.setText(Util.getInstance().getUsername());
            txtUrl.setText(Util.getInstance().getUrl());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }
