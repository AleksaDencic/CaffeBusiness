    private void btnPokreniServerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPokreniServerActionPerformed
        
        try {
            btnIzbaciKlijenta.setEnabled(true);
            btnIzbaciKlijenta.setVisible(true);
            btnPokreniServer.setEnabled(false);
            btnZaustaviServer.setEnabled(true);
            panelTblKlijenti.setVisible(true);
            Util.getInstance().setUrl(txtUrl.getText().trim());
            Util.getInstance().setPassword(String.valueOf(txtPassword.getPassword()).trim());
            Util.getInstance().setUsername(txtUsername.getText().trim());
            txtUrl.setEnabled(false);
            txtPassword.setEnabled(false);
            txtUsername.setEnabled(false);
            server = new ThreadServer(9009, (ModelTblKlijenti) tblKlijenti.getModel());
            server.start();
            JOptionPane.showMessageDialog(this, "Server je pokrenut.");
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(rootPane, "Greska: Nije moguce pokrenuti server.");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        } 
    }//GEN-LAST:event_btnPokreniServerActionPerformed

    private void btnZaustaviServerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnZaustaviServerActionPerformed
        
        try {
            btnPokreniServer.setEnabled(true);
            btnZaustaviServer.setEnabled(false);
            txtUrl.setEnabled(true);
            txtPassword.setEnabled(true);
            txtUsername.setEnabled(true);
            btnIzbaciKlijenta.setEnabled(false);
            server.interrupt();
            ((ThreadServer) server).stopThread();
            ModelTblKlijenti mtk = (ModelTblKlijenti) tblKlijenti.getModel();
            mtk.izbaciSveKlijente();
            mtk.fireTableDataChanged();
            JOptionPane.showMessageDialog(rootPane, "Server zaustavljen.");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }//GEN-LAST:event_btnZaustaviServerActionPerformed


